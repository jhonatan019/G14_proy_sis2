    private void agregarEventos() {
        botonSelecCercado.addActionListener(e -> abrirListaPeliculas("CERCADO", 3));
        botonSelecQuillacollo.addActionListener(e -> abrirListaPeliculas("QUILLACOLLO", 2));
        botonSelecSacaba.addActionListener(e -> abrirListaPeliculas("SACABA", 1));
    }

    private void abrirListaPeliculas(String nombreSucursal, int idSucursal) {
        // Guardamos la sucursal seleccionada
        listaPeliculas frame;
        switch (idSucursal) {
            case 3:
                frame = new listaPeliculasCercado(nombreSucursal, idSucursal);
                break;
            case 2:
                frame = new listaPeliculasQuillacollo(nombreSucursal, idSucursal);
                break;
            case 1:
                frame = new listaPeliculasSacaba(nombreSucursal, idSucursal);
                break;
            default:
                return;
        }

        frame.setVisible(true);
        this.dispose();
    }
        
